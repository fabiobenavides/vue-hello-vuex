<template>
  <img alt="Vue logo" src="./assets/logo.png">
  <div class="counter">
    {{ counter }}
  </div>
  <div class="buttons">
    <button @click="decreaseCounter">-</button>
    <button @click="increaseCounter">+</button>
  </div>
</template>

<script>

export default {
  name: 'App',
  data() {
    return {
      counter: 0
    }
  },
  methods: {
    increaseCounter() {
      this.counter++;
    },
    decreaseCounter() {
      this.counter--;
    }
  }
}
</script>

